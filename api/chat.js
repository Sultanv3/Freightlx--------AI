export default async function handler(req) {
  // Only allow POST
  if (req.method !== 'POST') {
    return new Response(JSON.stringify({ error: 'Method not allowed' }), {
      status: 405,
      headers: { 'Content-Type': 'application/json' }
    });
  }

  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    return new Response(JSON.stringify({
      error: 'GEMINI_API_KEY not configured',
      hint: 'Set GEMINI_API_KEY in Vercel project environment variables'
    }), {
      status: 500,
      headers: { 'Content-Type': 'application/json' }
    });
  }

  let body;
  try {
    body = await req.json();
  } catch {
    return new Response(JSON.stringify({ error: 'Invalid JSON' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' }
    });
  }

  const { message, history = [] } = body;
  if (!message || typeof message !== 'string') {
    return new Response(JSON.stringify({ error: 'Message required' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' }
    });
  }

  // Limit message length to prevent abuse
  if (message.length > 2000) {
    return new Response(JSON.stringify({ error: 'Message too long (max 2000 chars)' }), {
      status: 400,
      headers: { 'Content-Type': 'application/json' }
    });
  }

  // Build conversation history for Gemini
  const contents = [];
  // Add previous messages (limited to last 10 for context window)
  const recentHistory = (history || []).slice(-10);
  for (const msg of recentHistory) {
    if (msg.role && msg.content && typeof msg.content === 'string') {
      contents.push({
        role: msg.role === 'assistant' ? 'model' : 'user',
        parts: [{ text: msg.content.substring(0, 2000) }]
      });
    }
  }
  // Add new user message
  contents.push({
    role: 'user',
    parts: [{ text: message }]
  });

  const geminiUrl = `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash-lite:generateContent?key=${apiKey}`;

  try {
    const response = await fetch(geminiUrl, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        contents,
        systemInstruction: { parts: [{ text: SYSTEM_PROMPT }] },
        generationConfig: {
          temperature: 0.5,
          maxOutputTokens: 350,
          topP: 0.9
        },
        safetySettings: [
          { category: 'HARM_CATEGORY_HARASSMENT', threshold: 'BLOCK_MEDIUM_AND_ABOVE' },
          { category: 'HARM_CATEGORY_HATE_SPEECH', threshold: 'BLOCK_MEDIUM_AND_ABOVE' },
          { category: 'HARM_CATEGORY_SEXUALLY_EXPLICIT', threshold: 'BLOCK_MEDIUM_AND_ABOVE' },
          { category: 'HARM_CATEGORY_DANGEROUS_CONTENT', threshold: 'BLOCK_MEDIUM_AND_ABOVE' }
        ]
      })
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('Gemini API error:', response.status, errorText);
      return new Response(JSON.stringify({
        error: 'AI service error',
        status: response.status
      }), {
        status: 502,
        headers: { 'Content-Type': 'application/json' }
      });
    }

    const data = await response.json();
    let text = data?.candidates?.[0]?.content?.parts?.[0]?.text || '';

    // ================================================================
    // POST-PROCESSING: Strip ALL "تحقق البيانات" disclaimer blocks
    // ================================================================
    // Gemini keeps inserting a multi-line verification block. We nuke it.

    // 1) Kill the whole "تحقق البيانات" section block (header + bullets)
    //    Matches from "تحقق البيانات" (with optional ** and :) through
    //    the last bullet point in the list (until empty line or EOF).
    const blockHeader = /(?:\*{0,2}\s*)?(?:📋|🔍|⚠️|✅|❗|💡)?\s*\*{0,2}\s*(?:تحقق\s+(?:من\s+)?البيانات|تحقّق\s+البيانات|التحقّق|verification)[\s\S]*?(?=\n\s*\n|\n[^\s•\-\*●▪◦]|$)/gi;
    text = text.replace(blockHeader, '');

    // 2) Kill any standalone disclaimer LINES (bullets or otherwise)
    //    that include the canonical "verification dashboard" phrases.
    const disclaimerLines = [
      /^[\s•\-\*●▪◦]*\*{0,2}\s*مستوى\s+الدقة[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*اعتمدت\s+على[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*المتوفر\s+الآن[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*غير\s+مؤكد\s+(?:بدون|إلا)[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*الخطوة\s+التالية[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*نسبة\s+الدقة[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*مصدر\s+المعلومة[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*درجة\s+الثقة[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*التحقّق\s+من[^\n]*$/gm,
      /^[\s•\-\*●▪◦]*\*{0,2}\s*ملاحظة\s*[:،][^\n]*$/gm
    ];
    for (const r of disclaimerLines) text = text.replace(r, '');

    // 3) Kill old-style inline disclaimer phrases
    const inlinePhrases = [
      /تحقق\s+من\s+البيانات[^.!\n]*[.!\n]?/g,
      /تحقّق\s+(?:من\s+)?البيانات[^.!\n]*[.!\n]?/g,
      /تأكد\s+من\s+البيانات[^.!\n]*[.!\n]?/g,
      /راجع\s+البيانات[^.!\n]*[.!\n]?/g,
      /هذه\s+(?:البيانات|الأسعار|المعلومات)\s+تقديرية[^.!\n]*[.!\n]?/g,
      /هذه\s+المعلومات\s+للاطلاع\s+فقط[^.!\n]*[.!\n]?/g,
      /للتأكد\s+ارجع\s+لمصدر\s+رسمي[^.!\n]*[.!\n]?/g,
      /تواصل\s+مع\s+مختص[^.!\n]*[.!\n]?/g,
      /استشر\s+مختصاً?[^.!\n]*[.!\n]?/g,
      /\*+\s*هذه\s+أسعار\s+تقديرية[^*]*\*+/g,
      /\*+\s*ملاحظة\s*:[^*]*\*+/g
    ];
    for (const phrase of inlinePhrases) text = text.replace(phrase, '');

    // 4) Final cleanup — collapse blank lines, trim, etc.
    text = text
      .replace(/\n{3,}/g, '\n\n')
      .replace(/[ \t]{2,}/g, ' ')
      .replace(/^[\s•\-\*]*$/gm, '')   // strip orphaned bullet markers
      .replace(/\n{3,}/g, '\n\n')
      .trim();

    if (!text) {
      return new Response(JSON.stringify({
        error: 'Empty response',
        finishReason: data?.candidates?.[0]?.finishReason
      }), {
        status: 502,
        headers: { 'Content-Type': 'application/json' }
      });
    }

    return new Response(JSON.stringify({
      reply: text,
      model: 'gemini-2.5-flash-lite'
    }), {
      status: 200,
      headers: {
        'Content-Type': 'application/json',
        'Cache-Control': 'no-store'
      }
    });
  } catch (err) {
    console.error('Chat API error:', err);
    return new Response(JSON.stringify({
      error: 'Internal server error',
      message: err.message
    }), {
      status: 500,
      headers: { 'Content-Type': 'application/json' }
    });
  }
}

