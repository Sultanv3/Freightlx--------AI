  // ════════════════════════════════════════════════════════════
  // 8️⃣ SMART DEFAULT — analyze what we DID parse, ask clarifying question
  // ════════════════════════════════════════════════════════════
  // Detect partial intents and respond contextually
  const wordCount = norm.split(/\s+/).filter(Boolean).length;
